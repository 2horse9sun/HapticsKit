import CoreHaptics

public struct HKHeartbeatsPattern: HKHapticPattern {
    public let name = "Heartbeats"
    public let detail = "Three organic heartbeats over three seconds, made using precisely spaced transient events at varying parameters."
    public let category: HKHapticPatternCategory = .static
    
    public init() {
        
    }
    
